#include "libft.h"

void	ft_putstr(char *str, int *len)
{
	int	a;

	a = 0;
	if (!str)
	{
		*len += write(1, "(null)", 6);
		return ;
	}
	while (str[a] != '\0')
	{
		*len += write(1, &str[a], 1);
		++a;
	}
}
