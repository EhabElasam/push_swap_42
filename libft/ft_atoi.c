#include "libft.h"

int	ft_atoi(const char *str)
{
	int		s;
	long	r;

	r = 0;
	s = 1;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			s = -s;
		str++;
		if (*str == '-' || *str == '+')
			return (0);
	}
	while (*str != '\0' && (*str >= '0' && *str <= '9'))
	{
		r = (r * 10) + (*str - '0');
		str++;
	}
	if (r * s > 2147483647 || r * s < -2147483648
		|| (*str != '\0' && *str != ' '))
		return (0);
	return ((int)(r * s));
}
